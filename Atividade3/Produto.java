
public class Produto {
	public int Ids;
	public String Nomes;
	public String Tipos;
	public String Marcas;
	public String Descrições;

	public int getIds() {
		return Ids;
	}

	public void setIds(int ids) {
		Ids = ids;
	}

	public String getNomes() {
		return Nomes;
	}

	public void setNomes(String nomes) {
		Nomes = nomes;
	}

	public String getTipos() {
		return Tipos;
	}

	public void setTipos(String tipos) {
		Tipos = tipos;
	}

	public String getMarcas() {
		return Marcas;
	}

	public void setMarcas(String marcas) {
		Marcas = marcas;
	}

	public String getDescrições() {
		return Descrições;
	}

	public void setDescrições(String descrições) {
		Descrições = descrições;
	}
}
